#comparison operators 

x = 20 
y = 11 

#is equal ?
print (x == y)
# not equal 
print (x != y)
# greaterthan 
print (x >  y )
# lessthan 
print (x < y)
# greaterthan or equal
print (x >=  y )
# lessthan or equal 
print (x <= y)


# operator and &
print ( x >  2 & x < 400 )
# operator or  || 
print ( x >  2 or x < 400 )
# not operator 
print (not (x == 57 and x > 12))