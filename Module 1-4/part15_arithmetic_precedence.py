x = 32 
y = 7

# add 
print(x + y)  # Output: 39

# subtraction 
print(x - y)  # Output: 25

# multiply 
print(x * y)  # Output: 224

# divide 
print(x / y)  # Output: 4.571428571428571

# modulo 
print(x % y)  # Output: 4

# exponent 
print(x ** y)  # Output: 893871739

# floor division 
print(x // y)  # Output: 4

